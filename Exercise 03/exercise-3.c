#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main() {
    // Declare string variable
    char str[] = "Welcome to course";
    
    // Declare pointer variable with initial value
    char* ptr = "Welcome to course";
    
    printf("Using array: %s\n", str);
    printf("Using pointer: %s\n", ptr);
    
    return 0;
}
